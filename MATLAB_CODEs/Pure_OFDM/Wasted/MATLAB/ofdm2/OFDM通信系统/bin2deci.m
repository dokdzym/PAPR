%bin2deci.m
function y=bin2deci(x)
% 将二进制数转化为十进制数

%+++++++++++++++++++++++variables++++++++++++++++++++++++++++
% x        输入数据
% y        输出数据
%++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ 

t=length(x);
y=(t-1:-1:0);
y=2.^y;
y=x*y';
% ************************end of file*********************************