%deci2bin.m
function y=deci2bin(x,t)
% 十进制数 x 转化为二进制数，二进制数至少表示为 t 位

%+++++++++++++++++++++++variables++++++++++++++++++++++++++++
% x         输入数据
% t         二进制数位数
% y         输出数据
%++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++ 

y=zeros(1,t);
i=1;
while x>=0&i<=t
y(i)=rem(x,2);
x=(x-y(i))/2;
i=i+1;
end
y=y(t:-1:1);
% ************************end of file**********************************